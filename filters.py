"""Защита от мусора до n8n.

Отсекаем рекламу, курсы/инфобизнес, HR-маркетинг и спам, чтобы не гонять
их через AI-агент. Используется во всех источниках (Telegram, HH, RemoteOK).

Подход — скоринг по ключевым словам: у каждого паттерна свой вес, текст
считается мусором, когда сумма весов достигает порога. Так один сильный
маркер (например "erid", "вебинар") отсекает сразу, а слабые
(«курс», «обучение», «маркетинг») срабатывают только в паре — чтобы не
зарубить настоящую вакансию, где такое слово встретилось мимоходом.
"""

import re

THRESHOLD = 5

# (regex, вес). Текст приводится к нижнему регистру перед матчингом.
# Сильные маркеры (вес >= THRESHOLD) отсекают пост в одиночку.
PATTERNS = [
    # --- Явная реклама / маркировка ---
    (r"\berid\b", 5),                       # обязательная маркировка рекламы в РФ
    (r"промокод", 5),
    (r"реклам(?:а|ы|е|ный|одател)", 5),
    (r"рассылк[аи]\b", 5),
    (r"розыгрыш", 5),
    (r"скидк\w*\s*\d", 5),                   # «скидка 50%»
    (r"\bэфир(?!иум)\w*", 5),                # «эфир начнётся через 2 часа» (не «эфириум»)
    (r"\bгрант\w*", 5),                      # «получи грант» (не «иммигрант»)

    # --- Курсы / инфобизнес ---
    (r"вебинар", 5),
    (r"\bбуткемп\b", 5),
    (r"\bbootcamp\b", 5),
    (r"наставничеств|менторств|менторск", 5),
    (r"инфопродукт", 5),
    (r"осво(?:й|ить|ишь)\s+профессию", 5),
    (r"с\s+нуля\s+до\s+(?:сеньора|оффера|зарплат|джуна|джуниора|middle|junior)", 5),
    (r"гаранти\w*\s+трудоустройств", 5),
    (r"научим\s+с\s+нуля", 5),

    # --- Заработок / скам ---
    (r"пассивн\w+\s+доход", 5),
    (r"казино|ставки\s+на\s+спорт|\bфорекс\b|бинарн\w+\s+опцион", 5),
    (r"крипто(?:валют|инвест)", 5),

    # --- Слабые маркеры (нужны минимум два) ---
    (r"\bкурс(?:ы|а|ов|е|у)?\b", 2),
    (r"обучени[ея]", 2),
    (r"\bскидк\w*", 2),
    (r"успей\b", 2),
    (r"подпис(?:ка|ывайся|ался|ывайтесь)|подпишись", 2),
    (r"научи(?:м|сь|тесь)", 2),
    (r"маркетинг", 2),
    (r"\bhr\b|эйчар|рекрут", 2),
    (r"бесплатн\w+\s+(?:урок|интенсив|мастер-?класс|марафон)", 2),
    (r"только\s+сегодня|спешите|жми\b|переходи\s+по\s+ссылке", 2),
]

_COMPILED = [(re.compile(p, re.IGNORECASE), w) for p, w in PATTERNS]


def garbage_reason(text: str):
    """Вернуть причину (список сработавших паттернов + счёт) или None."""
    if not text:
        return None

    hits = []
    score = 0
    for rx, weight in _COMPILED:
        m = rx.search(text)
        if m:
            hits.append(m.group(0).strip())
            score += weight

    if score >= THRESHOLD:
        return f"score={score}: " + ", ".join(hits)
    return None


def is_garbage(text: str) -> bool:
    return garbage_reason(text) is not None
